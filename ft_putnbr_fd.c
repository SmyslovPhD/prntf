#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	int		m;
	char	c;

	m = n < 0 ? -1 : 1;
	while ((long)n / m > 9)
		m *= 10;
	if (n < 0)
		ft_putchar_fd('-', fd);
	while (m != 0)
	{
		c = (long)n / m % 10 + '0';
		ft_putchar_fd(c, fd);
		m /= 10;
	}
}
