#include "libft.h"

int	ft_order_base(int nbr, int base)
{
	int	i;

	if (base <= 1)
		return (-1);
	i = 0;
	while (nbr >= base || nbr <= -base)
	{
		nbr /= base;
		i++;
	}
	return (i);
}
